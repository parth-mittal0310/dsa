#include<bits/stdc++.h>
#include<iostream>
using namespace std;
int leftrotate(int arr[],int n,int d)
{
    d = d % n;
    int temp[d];
    for(int i=0;i<d;i++ )
    {
        temp[i] = arr[i];
    }
    for(int i=d; i<n;i++)
    {
        arr[i-d] = arr[i];
    }
    for(int i=n-d;i<n;i++)
    {
        arr[i] = temp[(i-(n-d))];
    }
}
int main()
{
    int n,d;
    int arr[50];
    cout<<"enter the value of n::";
    cin>>n;
    for(int i=0 ;i<n; i++)
    {
        cin>>arr[i];
    }
    cout<<"enter the value of d::";
    cin>>d;
    leftrotate(arr,n,d);
    for(int i=0;i<n;i++)
    {
        cout<<arr[i]<<"  ";
    }

    

}